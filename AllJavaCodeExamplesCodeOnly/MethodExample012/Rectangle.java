public class Rectangle {

	/*-
	 * Rectangle
	 * External Methods for Rectangle Properties
	 * Author:  Don Spickler
	 * Date: 3/7/2011
	 */

	public static double Area(double length, double width) {
		return length * width;
	}

	public static double Perimeter(double length, double width) {
		return 2 * length + 2 * width;
	}
}
