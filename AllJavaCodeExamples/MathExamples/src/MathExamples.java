public class MathExamples {

	/*-
	 * MathExamples --- just some calculations.
	 * Author:  Don Spickler
	 * Date: 2/2/2011
	 */

	public static void main(String[] args) {
		double num1 = Math.random();
		System.out.println("Random Number: " + num1);
		System.out.println("e =  " + Math.E);
		System.out.println("pi = " + Math.PI);
		System.out.println("floor(27.8) = " + Math.floor(27.8));
		System.out.println("floor(17.0) = " + Math.floor(17.0));
		System.out.println("floor(-7.6) = " + Math.floor(-7.6));
		System.out.println("sqrt(2) = " + Math.sqrt(2));
		System.out.println("sqrt(16) = " + Math.sqrt(16));
		System.out.println("1/3 = " + (1 / 3));
		System.out.println("4/3 = " + (4 / 3));
		System.out.println("1.0/3.0 = " + (1.0 / 3.0));
		System.out.println("1.0/3 = " + (1.0 / 3));
		System.out.println("1/3.0 = " + (1 / 3.0));
		System.out.println("1.0/3.0*3.0 = " + (1.0 / 3.0) * 3.0);
		System.out.println("max(23, 102) = " + Math.max(23, 102));
		System.out.println("min(23, 102) = " + Math.min(23, 102));
	}
}
